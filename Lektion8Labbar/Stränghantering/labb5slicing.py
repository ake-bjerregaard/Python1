


lista = [0, 1, 2, 3, 4, 5, 6]

print(lista[4]) #vanlig indexering

print(lista[2:4]) #här slicing som tar element 2 och 3

tuppel = (0, 1, 2, 3, 4, 5)

print(tuppel[2:4]) #här slicing som tar element 2 och 3 från en tuppel

print(lista[::-1]) #slicing som reverse returnerar listan baklänges

sträng = "Hello world!"

print(sträng[4])
print(sträng[6:11]) #slicing funkar även på stränger # tar ut ordet world
print(sträng[6:]) #börjar på index 6 och tar med resten av strängen
print(sträng[::-1]) #reverse sträng


